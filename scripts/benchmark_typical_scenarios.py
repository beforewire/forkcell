def default_runtime_env(repo: Path, env: dict[str, str], backend: str) -> dict[str, str]:
    if backend not in {"native-overlay", "volume-delta"}:
        return env
    env = dict(env)
    env.setdefault("OPENSHELL_GATEWAY_ENDPOINT", "http://127.0.0.1:17671")
    lock_path = repo / ".forkcell" / "runtime-lock.json"
    if "FORKCELL_OPENSHELL_BIN" not in env and lock_path.exists():
        try:
            lock = json.loads(lock_path.read_text())
            openshell = ((lock.get("binaries") or {}).get("openshell") or {}).get("path")
            if openshell:
                env["FORKCELL_OPENSHELL_BIN"] = openshell
        except json.JSONDecodeError:
            pass
    return env


def ms_now() -> int:
    return time.time_ns() // 1_000_000


def run(cmd: list[str], *, cwd: Path, env: dict[str, str] | None = None, check: bool = True) -> subprocess.CompletedProcess[str]:
    return subprocess.run(cmd, cwd=cwd, env=env, check=check, text=True, stdout=subprocess.PIPE, stderr=subprocess.PIPE)


def write_text_file(path: Path, size: int, seed: int) -> None:
    base = f"// seed={seed}\nexport const value = '{'x' * 80}';\n"
    path.write_text((base * (size // len(base) + 1))[:size], encoding="utf-8")


def make_workspace(root: Path, scenario: dict[str, Any]) -> None:
    if root.exists():
        shutil.rmtree(root)
    root.mkdir(parents=True)
    top_names = ["src", "tests", "docs", "config", "scripts"]
    for d in range(int(scenario["dirs"])):
        sub = root / top_names[d % len(top_names)] / f"pkg_{d:03d}"
        sub.mkdir(parents=True, exist_ok=True)
        for f in range(int(scenario["files_per_dir"])):
            ext = [".ts", ".py", ".md", ".json", ".txt"][f % 5]
            write_text_file(sub / f"file_{f:03d}{ext}", int(scenario["bytes_per_file"]), d * 100_000 + f)
    cache = root / "cache" / "artifacts"
    cache.mkdir(parents=True, exist_ok=True)
    for i in range(int(scenario["blob_files"])):
        with (cache / f"blob_{i:03d}.bin").open("wb") as fh:
            remaining = int(scenario["blob_bytes"])
            chunk = bytes([i % 251]) * 4096
            while remaining:
                data = chunk[: min(len(chunk), remaining)]
                fh.write(data)
                remaining -= len(data)


def workspace_stats(root: Path) -> dict[str, int]:
    files = dirs = bytes_ = 0
    for path in root.rglob("*"):
        if path.is_dir():
            dirs += 1
        elif path.is_file():
            files += 1
            bytes_ += path.stat().st_size
    return {"files": files, "dirs": dirs, "bytes": bytes_}


def mutation_shell(scenario: dict[str, Any]) -> str:
    modify = int(scenario["modify"])
    add = int(scenario["add"])
    delete = int(scenario["delete"])
    return f"""
set -eu
i=0
for f in $(find . -type f | sort | head -n {modify}); do
  printf '\n# agent mutation\n' >> "$f"
  i=$((i+1))
done
for f in $(find . -type f | sort | head -n {modify + delete} | tail -n {delete}); do
  rm -f "$f"
done
mkdir -p agent-output
i=0
while [ "$i" -lt {add} ]; do
  printf 'generated by failing agent run\n' > "agent-output/generated_$i.txt"
  i=$((i+1))
done
exit 7
""".strip()


def latest_receipt(repo: Path) -> Path:
    receipts = sorted((repo / ".forkcell" / "receipts").glob("rcpt_*.json"), key=lambda p: p.stat().st_mtime)
    if not receipts:
        raise RuntimeError("no ForkCell receipt found")
    return receipts[-1]


def init_cell(args: argparse.Namespace, cell: str, workspace: Path, env: dict[str, str]) -> tuple[dict[str, Any], int]:
    start = ms_now()
    if args.backend == "local-overlay":
        proc = run([*args.forkcell_cmd, "overlay", "init", cell, "--from", str(workspace)], cwd=args.repo, env=env)
    elif args.backend == "volume-delta":
        proc = run([*args.forkcell_cmd, "volume", "init", cell, "--from", str(workspace)], cwd=args.repo, env=env)
    elif args.backend == "native-overlay":
        proc = run([*args.forkcell_cmd, "native", "init", cell, "--from", str(workspace)], cwd=args.repo, env=env)
    else:
        raise AssertionError(args.backend)
    return json.loads(proc.stdout), ms_now() - start


def delete_cell(args: argparse.Namespace, cell: str, env: dict[str, str]) -> None:
    if args.backend == "local-overlay":
        cmd = [*args.forkcell_cmd, "overlay", "delete", cell]
    elif args.backend == "volume-delta":
        cmd = [*args.forkcell_cmd, "volume", "delete", cell]
    else:
        cmd = [*args.forkcell_cmd, "native", "delete", cell]
    run(cmd, cwd=args.repo, env=env, check=False)


def run_cell(args: argparse.Namespace, cell: str, scenario: dict[str, Any], env: dict[str, str]) -> int:
    if args.backend == "local-overlay":
        cmd = [*args.forkcell_cmd, "overlay", "run", "--checkpoint-before", "--restore-on-fail", cell, "--", "sh", "-lc", mutation_shell(scenario)]
    elif args.backend == "volume-delta":
        cmd = [*args.forkcell_cmd, "volume", "run", "--checkpoint-before", "--restore-on-fail", cell, "--", "sh", "-lc", mutation_shell(scenario)]
    else:
        cmd = [*args.forkcell_cmd, "native", "run", "--checkpoint-before", "--restore-on-fail", cell, "--", "sh", "-lc", mutation_shell(scenario)]
        if args.sync_logs:
            cmd.insert(cmd.index("run") + 1, "--sync-logs")
    start = ms_now()
    run(cmd, cwd=args.repo, env=env, check=False)
    return ms_now() - start


def summarize_receipt(args: argparse.Namespace, receipt: dict[str, Any]) -> dict[str, Any]:
    before = (receipt.get("checkpoints") or {}).get("before_metrics") or {}
    restore = (receipt.get("checkpoints") or {}).get("restore_metrics") or {}
    breakdown = restore.get("breakdown") or {}
    details = restore.get("details") or {}
    checkpoint_host = before.get("duration_ms")
    checkpoint_inner = before.get("inner_duration_ms")
    return {
        "checkpoint_host_ms": checkpoint_host,
        "checkpoint_fs_inner_ms": checkpoint_inner,
        "checkpoint_docker_cli_ms": None if checkpoint_host is None or checkpoint_inner is None else max(0, checkpoint_host - checkpoint_inner),
        "checkpoint_hashed_files": before.get("hashed_files"),
        "checkpoint_new_objects": before.get("new_objects"),
        "restore_host_ms": restore.get("duration_ms"),
        "restore_sync_ms": breakdown.get("restore_sync_ms"),
        "restore_call_ms": breakdown.get("restore_call_ms"),
        "sandbox_delete_ms": breakdown.get("sandbox_delete_ms"),
        "sandbox_lifecycle_ms": breakdown.get("sandbox_lifecycle_ms"),
        "log_collect_ms": breakdown.get("log_collect_ms"),
        "total_restore_path_ms": breakdown.get("total_restore_path_ms"),
        "restore_copied_files": details.get("copied_files"),
        "restore_reused_files": details.get("reused_files"),
        "restore_removed_paths": details.get("removed_paths"),
        "decision": (receipt.get("decision") or {}).get("result"),
        "exit_code": (receipt.get("run") or {}).get("exit_code"),
        "log_collection_mode": (receipt.get("policy") or {}).get("log_collection", {}).get("mode"),
    }


def median_or_none(rows: list[dict[str, Any]], key: str) -> int | None:
    values = [row[key] for row in rows if isinstance(row.get(key), (int, float))]
    return int(median(values)) if values else None


def main() -> int:
    parser = argparse.ArgumentParser(description="Run ForkCell typical agent-workspace benchmarks.")
    parser.add_argument("--backend", choices=["local-overlay", "volume-delta", "native-overlay"], default="local-overlay")
    parser.add_argument("--repo", type=Path, default=Path.cwd())
    parser.add_argument("--workdir", type=Path, default=None)
    parser.add_argument("--iterations", type=int, default=3)
    parser.add_argument("--scenario", action="append", choices=[s["name"] for s in SCENARIOS], help="run only selected scenario(s)")
    parser.add_argument("--forkcell-cmd", nargs="+", default=[sys.executable, "-m", "forkcell.cli"])
    parser.add_argument("--sync-logs", action="store_true", help="use synchronous native-overlay log collection")
    ns = parser.parse_args()
    ns.repo = ns.repo.resolve()
    workdir = ns.workdir or Path(f"/tmp/forkcell-typical-{ns.backend}-{int(time.time())}")
    if workdir.exists():
        shutil.rmtree(workdir)
    workdir.mkdir(parents=True)
    env = default_runtime_env(ns.repo, os.environ.copy(), ns.backend)
    rows: list[dict[str, Any]] = []
    selected = [s for s in SCENARIOS if not ns.scenario or s["name"] in set(ns.scenario)]
    for scenario in selected:
        for index in range(ns.iterations):
            cell = f"bench-{ns.backend.replace('-', '')}-{scenario['name']}-{index}-{uuid_suffix()}"
            workspace = workdir / f"workspace-{scenario['name']}-{index}"
            make_workspace(workspace, scenario)
            stats = workspace_stats(workspace)
            delete_cell(ns, cell, env)
            init_json, init_wall_ms = init_cell(ns, cell, workspace, env)
            run_wall_ms = run_cell(ns, cell, scenario, env)
            receipt_path = latest_receipt(ns.repo)
            receipt = json.loads(receipt_path.read_text())
            row = {
                "backend": ns.backend,
                "scenario": scenario["name"],
                "description": scenario["description"],
                "iteration": index + 1,
                "files": stats["files"],
                "dirs": stats["dirs"],
                "bytes": stats["bytes"],
                "mutation": f"M{scenario['modify']}/A{scenario['add']}/D{scenario['delete']}",
                "init_wall_ms": init_wall_ms,
                "init_import_ms": init_json.get("import_ms"),
                "run_wall_ms": run_wall_ms,
                "restore_correct": not (workspace / "agent-output").exists(),
                "receipt": str(receipt_path),
                **summarize_receipt(ns, receipt),
            }
            rows.append(row)
            print(json.dumps(row, ensure_ascii=False, sort_keys=True), flush=True)
            delete_cell(ns, cell, env)
    raw = workdir / "typical-results.jsonl"
    raw.write_text("\n".join(json.dumps(row, ensure_ascii=False, sort_keys=True) for row in rows) + "\n")
    print(f"RAW_JSONL={raw}")
    print()
    print(f"# ForkCell Typical Benchmark ({ns.backend})")
    print()
    print("| scenario | files | MiB | mutation | init median ms | run median ms | checkpoint median ms | restore median ms | restore sync median ms | sandbox lifecycle median ms | log collect median ms | total restore path median ms | correctness |")
    print("|---|---:|---:|---|---:|---:|---:|---:|---:|---:|---:|---:|---|")
    for scenario in selected:
        group = [row for row in rows if row["scenario"] == scenario["name"]]
        first = group[0]
        ok = f"{sum(1 for row in group if row['restore_correct'])}/{len(group)}"
        print(
            "| {scenario} | {files} | {mib:.1f} | {mutation} | {init} | {run} | {ckpt} | {restore} | {restore_sync} | {sandbox} | {logs} | {total} | {ok} |".format(
                scenario=scenario["name"],
                files=first["files"],
                mib=first["bytes"] / 1048576,
                mutation=first["mutation"],
                init=median_or_none(group, "init_wall_ms"),
                run=median_or_none(group, "run_wall_ms"),
                ckpt=median_or_none(group, "checkpoint_host_ms"),
                restore=median_or_none(group, "restore_host_ms"),
                restore_sync=median_or_none(group, "restore_sync_ms"),
                sandbox=median_or_none(group, "sandbox_lifecycle_ms"),
                logs=median_or_none(group, "log_collect_ms"),
                total=median_or_none(group, "total_restore_path_ms"),
                ok=ok,
            )
        )
    return 0


def uuid_suffix() -> str:
    import uuid

    return uuid.uuid4().hex[:6]


if __name__ == "__main__":
    raise SystemExit(main())
